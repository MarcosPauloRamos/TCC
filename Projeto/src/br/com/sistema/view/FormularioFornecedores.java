    private void btnPesquisaNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaNomeActionPerformed
         // Aqui esta a função que lista todos os fornecedores no banco de dados
        //a função esta em clientes dao com o nome listar
        //Diferente da função listar, essa função lista apenas os nomes que 
        //coloca na pesquisa
        //a diferença agora é que não precisa mais clicar no botão pesquisar
        //conforme for digitando o nome, vai aparecendo os resultados
        String nome = "%"+txtPesquisaNome.getText()+"%";
        
        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> lista = dao.FiltrarFornecedor(nome);
        DefaultTableModel dados = (DefaultTableModel) tabela.getModel();
        dados.setNumRows(0);
        for(Fornecedores f : lista){
            dados.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCNPJ(),
                f.getEmail(),
                f.getTelefone(),
                f.getCelular(),
                f.getCep(),
                f.getEndereco(),
                f.getNumero(),
                f.getComplemento(),
                f.getBairro(),
                f.getCidade(),
                f.getEstado()
            });
        }
    }//GEN-LAST:event_btnPesquisaNomeActionPerformed

