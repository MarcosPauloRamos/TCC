    public void listar(){
        // Aqui esta a função que lista todos os fornecedores no banco de dados
        //a função esta em fornecedores dao com o nome listar
        //o problema desta função é que para listar tudo precisa estar limpando
        //a pesquisa e clicar em pesquisar
        FornecedoresDAO dao = new FornecedoresDAO();
        List<Fornecedores> lista = dao.ListarFornecedor();
        DefaultTableModel dados = (DefaultTableModel) tabela.getModel();
        dados.setNumRows(0);
        for(Fornecedores f : lista){
            dados.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCNPJ(),
                f.getEmail(),
                f.getTelefone(),
                f.getCelular(),
                f.getCep(),
                f.getEndereco(),
                f.getNumero(),
                f.getComplemento(),
                f.getBairro(),
                f.getCidade(),
                f.getEstado()
            });
        }
    }
    
